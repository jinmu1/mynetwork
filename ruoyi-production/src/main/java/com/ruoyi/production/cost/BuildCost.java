package com.ruoyi.production.cost;

/**
 * 建设成本
 */
public class BuildCost {
    private double  midbin;//中间仓面积
    private double sidebin;//线边仓面积


    public double getMidbin() {
        return midbin;
    }

    public void setMidbin(double midbin) {
        this.midbin = midbin;
    }

    public double getSidebin() {
        return sidebin;
    }

    public void setSidebin(double sidebin) {
        this.sidebin = sidebin;
    }
}
